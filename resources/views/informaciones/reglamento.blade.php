                        <strong>Horarios y asistencia</strong>
                        <ol>
                            <li>Los horarios de entrenamiento se establecerán y comunicarán con anticipación. Los estudiantes deben llegar a tiempo y estar preparados para iniciar la clase puntualmente.</li>
                            <li>La asistencia a las clases es obligatoria. En caso de ausencia por razones justificadas, se debe informar al instructor con anticipación.</li>
                        </ol>
                    </li>
                    <li>
                        <strong>Conducta y disciplina</strong>
                        <ol>
                            <li>Se espera que todos los estudiantes mantengan un comportamiento respetuoso y disciplinado en todo momento, tanto dentro como fuera de la escuela.</li>
                            <li>Está prohibido cualquier tipo de violencia, agresión física o verbal, bullying o comportamiento intimidante. El respeto hacia los compañeros y los instructores es fundamental.</li>
                            <li>El uso de lenguaje ofensivo, grosero o inapropiado está estrictamente prohibido.</li>
                            <li>Se prohíbe el consumo de alcohol, drogas o cualquier sustancia que pueda afectar la concentración y el rendimiento durante las clases.</li>
                        </ol>
                    </li>
                    <li>
                        <strong>Vestimenta y equipo</strong>
                        <ol>
                            <li>Todos los estudiantes deben utilizar el uniforme adecuado durante las clases y eventos oficiales de la escuela. La vestimenta debe estar limpia y en buen estado.</li>
                            <li>El uso de equipo de protección es obligatorio durante las prácticas que lo requieran, como guantes, espinilleras, protector bucal, entre otros.</li>
                            <li>Se debe mantener el equipo y las instalaciones en buen estado y reportar cualquier daño o desperfecto al instructor o personal administrativo.</li>
                            <li>Se debe mantener la higiene personal acorde: <strong class="text-danger">uñas cortas, ropa de entrenamiento limpia</strong></li>
                        </ol>
                    </li>
                    <li>
                        <strong>Seguridad</strong>
                        <ol>
                            <li>La seguridad es una prioridad en la escuela. Los estudiantes deben seguir las instrucciones del instructor en cuanto a técnicas y movimientos para evitar lesiones.</li>
                            <li>Queda estrictamente prohibido practicar cualquier técnica o ejercicio sin la supervisión adecuada de un instructor.</li>
                            <li>Los estudiantes deben informar al instructor sobre cualquier condición física o médica que pueda afectar su participación en las clases.</li>
                        </ol>
                    </li>
                    <li>
                        <strong>Uso de instalaciones</strong>
                        <ol>
                            <li>El uso de las instalaciones de la escuela debe ser adecuado y respetuoso. No se permiten comportamientos destructivos o desordenados.</li>
                            <li>Los estudiantes deben cuidar y mantener limpias las áreas de entrenamiento y los vestuarios.</li>
                            <li>El acceso a áreas restringidas está prohibido, a menos que se cuente con la autorización del instructor o personal administrativo.</li>
                        </ol>
                    </li>
                    <li>
                        <strong>Sanciones y medidas disciplinarias</strong>
                        <ol>
                            <li>El incumplimiento de este reglamento puede resultar en sanciones disciplinarias, que pueden incluir advertencias, suspensiones temporales o permanentes, o la expulsión de la escuela, dependiendo de la gravedad de la falta.</li>
                            <li>Las sanciones serán determinadas por el instructor principal y el personal administrativo de la escuela, y serán comunicadas al estudiante de manera oportuna.</li>
                        </ol>
                    </li>
